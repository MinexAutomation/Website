/**
 * Interface for RGB colors.
 */
export interface IColorRgb {
    /**
     * Red channel color (between 0 and 1).
     */
    R: number;
    /**
     * Green channel color (between 0 and 1).
     */
    G: number;
    /**
     * Blue channel color (between 0 and 1).
     */
    B: number;
}